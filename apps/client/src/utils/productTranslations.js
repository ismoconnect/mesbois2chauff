// Fonction pour traduire un nom de produit
export const translateProductName = (name, targetLang = 'fr') => {
    if (!name || targetLang === 'fr') return name;

    const terms = productTerms[targetLang];
    if (!terms) return name;

    let translatedName = name;

    // Remplacer chaque terme par sa traduction
    // On trie par longueur décroissante pour remplacer les termes les plus longs en premier
    const sortedTerms = Object.keys(productTerms.fr).sort((a, b) => b.length - a.length);

    sortedTerms.forEach(frenchTerm => {
        const translation = terms[frenchTerm];
        if (translation) {
            // Utiliser une regex avec le flag 'gi' pour remplacer toutes les occurrences (insensible à la casse)
            const regex = new RegExp(frenchTerm.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'), 'gi');
            translatedName = translatedName.replace(regex, translation);
        }
    });

    return translatedName;
};
